int	ft_atoi(char *str)
{
	int i;
	int sign;
	int resultat;

	i = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
			|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	sign = 1;
	while (str[i] == '-' || str[i] == '+')
		if (str[i] == '-')
			sign = sign * (-1);
	i++;
	resultat = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		resultat = resultat * 10 + str[i] - '0';
		i++;
	}
	return (sign * resultat);
}
