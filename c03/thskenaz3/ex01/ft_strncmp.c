int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int a;

	if (n == 0)
		return (0);
	a = 0;
	while (s1[a] != '\0' && s2[a] != '\0' && a < (n - 1))
	{
		if (s1[a] != s2[a])
			return (s1[a] - s2[a]);
		a++;
	}
	return (s1[a] - s2[a]);
}
