int		ft_iterative_factorial(int nb)
{
	int a;
	int b;

	a = nb;
	b = a - 1;
	if (a < 0)
		return (0);
	if (a == 0)
		return (1);
	while (b > 0)
	{
		a = a * b;
		b--;
	}
	return (a);
}
